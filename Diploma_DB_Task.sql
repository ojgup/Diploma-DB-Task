CREATE PROCEDURE ADD_PRODUCT @PPRODNAME NVARCHAR(100), @PBUYPRICE MONEY, @PSELLPRICE MONEY AS
BEGIN

   -- THE FOLLOWING MUST BE COMPLETED AS A SINGLE TRANSACTION
    BEGIN TRAN
    
    -- insert the specified values into the table PRODUCT2177
       INSERT INTO PRODUCT2177(PRODNAME, BUYPRICE,SELLPRICE) 
       VALUES (@PPRODNAME, @PBUYPRICE, @PSELLPRICE);
       
       DECLARE @PRODID INTEGER = @@IDENTITY;
       DECLARE @LOCATIONID NVARCHAR(8);
       
       DECLARE LOCATION_CURSOR CURSOR LOCAL FOR SELECT LOCATIONID FROM LOCATION2177;
    -- ADD A ROW FOR THIS PRODUCT TO THE INVENTORY2177 TABLE **FOR EACH** LOCTAION IN THE LOCATION2177 TABLE
       OPEN LOCATION_CURSOR;
       FETCH NEXT FROM LOCATION_CURSOR INTO @LOCATIONID
    -- I.E. IF THERE ARE 4 LOCATIONS THIS WILL BE 4 NEW ROWS IN THE INVENTORY TABLE
    -- RETURN THE NEW PRODUCTS PRODUCTID
       WHILE @@FETCH_STATUS = 0
       BEGIN
            INSERT INTO INVENTORY2177(PRODUCTID, LOCATIONID, NUMINSTOCK)
            VALUES(@PRODID, @LOCATIONID, 50);
            
            FETCH NEXT FROM LOCATION_CURSOR INTO @LOCATIONID
        END
        
        CLOSE LOCATION_CURSOR
        DEALLOCATE LOCATION_CURSOR

    COMMIT TRAN

    -- EXCEPTIONS
    -- for any other errors throw error : number 50000  message:  error_message()
END;


