CREATE PROCEDURE ADD_LOCATION @PLOCID NVARCHAR(8), @PLOCNAME NVARCHAR(50), @PLOCADDRESS NVARCHAR(200), @PMANAGER NVARCHAR(100) AS
BEGIN
    BEGIN TRY
    -- THE FOLLOWING MUST BE COMPLETED AS A SINGLE TRANSACTION
        BEGIN TRAN
    -- insert the specified values into the table LOCATION2177
        INSERT INTO LOCATION2177(LOCATIONID, LOCNAME, [ADDRESS], MANAGER) 
        VALUES (@PLOCID, @PLOCNAME, @PLOCADDRESS, @PMANAGER)
    -- ADD A ROW FOR THIS LOCATION TO THE INVENTORY2177 TABLE **FOR EACH** PRODUCT IN THE PRODUCT2177 TABLE
        DECLARE @PRODID INTEGER
        SET @PRODID = 0

        DECLARE product_cursor CURSOR LOCAL FOR SELECT PRODUCTID FROM PRODUCT2177;
        OPEN product_cursor;
        FETCH FROM product_cursor INTO @PRODID;

        WHILE @@fetch_status = 0
            BEGIN
                INSERT INTO INVENTORY2177(PRODUCTID, LOCATIONID, NUMINSTOCK) 
                VALUES (@PRODID, @PLOCID, 50);

                FETCH NEXT FROM product_cursor INTO @PRODID;
            END

        CLOSE product_cursor
        COMMIT TRAN
    -- I.E. IF THERE ARE 4 PRODUCTS THIS WILL BE 4 NEW ROWS IN THE INVENTORY TABLE
    -- RETURN THE LOCID OF THE NEW LOCATION
    END TRY
    -- EXCEPTIONS
    -- if the location id is a duplicate throw error: number 51001  message : 'Duplicate Location ID'
    -- for any other errors throw error : number 50000  message:  error_message()
    BEGIN CATCH

        IF ERROR_NUMBER() = 2627
            THROW 51001, 'DUPLICATE LOCATION ID', 1 
        ELSE 
            BEGIN 
                DECLARE @ERRORMESSAGE NVARCHAR(MAX) = ERROR_MESSAGE();
                THROW 50000, @ERRORMESSAGE, 1
            END;
    END CATCH;

    END;
GO

